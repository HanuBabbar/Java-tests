package test;

public class Switch {

	public static void main(String[] args) {
		
		String sweet = "Gulab jamun";
		switch(sweet) {
		case "Gulab jamun" : System.out.println("Gulab Jamun");
		break;
		case "Icecream" : System.out.println("Icecream");
		break;
		case "Rasgulla" : System.out.println("Rasgulla");
		break;
		default : System.out.println("No sweet");
		}

	}

}
